

/**
 * extension of building, has a list of occupants, a name and an address; mainly has teacher and student occupants
 * @author Kelly Olivier
 * @version 1.0
 */
public class School extends Building{
    /**
     * constructor for school
     * @param name name of school for building constructor
     * @param address name of address for building constructor
     */
    public School(String name, String address){
        super (name, address);
    }
}
